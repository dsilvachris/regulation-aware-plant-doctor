def evaluate_cross_dataset(model_to_test, target_classes_map):
    results = []
    for pd_cls, pv_cls in target_classes_map.items():
        path = os.path.join(pd_test_dir, pd_cls)
        if not os.path.exists(path):
            continue
        temp_ds = tf.keras.utils.image_dataset_from_directory(
            path,
            label_mode=None,
            image_size=(IMG_HEIGHT, IMG_WIDTH),
            batch_size=BATCH_SIZE,
            shuffle=False
        )   # NOTE: no /255 here — `model` already rescales internally

        preds = model_to_test.predict(temp_ds, verbose=0)
        pred_idx = np.argmax(preds, axis=1)

        if pv_cls in pv_class_names:
            target_idx = pv_class_names.index(pv_cls)
            accuracy = np.mean(pred_idx == target_idx)
            # what did the model MOST often predict for this class?
            counts = np.bincount(pred_idx, minlength=len(pv_class_names))
            top_pred = pv_class_names[counts.argmax()]
            results.append({
                "PlantDoc Class": pd_cls,
                "Accuracy": round(float(accuracy), 3),
                "Most common prediction": top_pred,
            })
    return pd.DataFrame(results)

