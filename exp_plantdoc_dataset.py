import hashlib

def hash_folder(folder, class_list):
    hashes = {}
    for c in class_list:
        for p in (folder / c).glob("*"):
            if p.suffix.lower() in {".jpg", ".jpeg", ".png"}:
                with open(p, "rb") as f:
                    h = hashlib.md5(f.read()).hexdigest()
                hashes.setdefault(h, []).append(str(p))
    return hashes

